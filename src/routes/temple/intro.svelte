<div class="wide-content space-top">
	<h2 class="title">「実験寺院」とは？</h2>
	<p class="body">「実験寺院」は、これまでの日本仏教における「宗門・宗派」とは文脈を異にする仏教のあり方を模索する中で生まれた概念です。</p>
	<p class="body">奈良時代以来、日本の歴史に受け継がれた各宗派は、それぞれに素晴らしい展開と発展を遂げてきましたが、現代に於いては「伝統」と「宗派組織」という枷をはめられて、身動きが取りにくくなっているように見受けられます。</p>
	<p class="body">日本の仏教思想には他所にはない大きな魅力と可能性がある。なのに、構造的にその威力が発揮できなくなっている。</p>
	<p class="body">この現状を打破し、仏教が本来の意義を発揮できる道はないのか。この問いに対するひとつの仮説が「実験寺院」でした。</p>
	<p class="body">旧来の「宗派」とは全く違う「宗教」でさえない「寺院」のありかた。</p>
	<p class="body">ブッダ＝シャカムニ以来の智慧を、大切に受け継ぎつつ実際の社会に即してアップデートを続けていく。</p>
	<p class="body">「そこに行けば哲学がある」</p>
	<p class="body">過剰な権威や厳めしい伝統、組織の論理など纏わない軽やかな「仏教思想の拠点」</p>
	<p class="body">シャカムニの頃の「寺院」はそんな場所だったかもしれません。</p>
</div>

